
public class TiposPrimitivos {

	public static void main(String[] args) {
		int tel = 1234;		
		int idade = 75;
		
		System.out.println(idade);
		System.out.println(tel);
		System.out.println(34);
		
		//Tipos primitivos
		int inteiro;
		boolean logico;
		float reais;
		
		char caractere;
		double reaisGrandes;
		long inteiroGrande;
		byte inteiroPequeno;
		short inteiroPequenos;
		
		String texto;
		
		//Tipos inteiros
		
		byte x1 = 100;
		short x2 = 1000; 
		int x3 = 1000000000;
		long x4 = 10000000000l;
		
		//Reais
		float y1 = 1.0f;
		double y2 = 10000.5;
		
		float y3 = 100000.f;
		float y4 = 100000.0f;
		
		double y5 = 10;
		double y6 = 10.;
		double y7 = 10.0;
		
		//Caractere
		char a1 = 'x';
		char a2 = 'y';
		char a3 = '\u0061';
		
		System.out.println(a3);
		
		//Logico
		boolean b1 = true;
		boolean b2 = false;
		
		//String nao e um tipo primitivo, e sim uma classe
		String nome = "Salu";
	}
}

